     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $tarea= Tarea::findOrFail($request->id);
        $tarea->name = $request->name;
